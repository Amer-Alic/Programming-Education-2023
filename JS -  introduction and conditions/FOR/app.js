// for loop = repeat something a certain number of times
// defined in three parts:
// for (PART ONE, PART TWO, PART THREE)

// FIRST PART: we declare a variable;
// PART TWO: we put the condition;
// PART THREE: we make some changes;

for (let counter = 10; counter >= 1; counter--) {
    console.log(counter)
}
